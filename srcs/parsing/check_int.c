#include "../../push_swap.h"

int	check_int(long *tab, int len)
{
	int	i;

	i = 0;
	while (i < len)
	{
		if (tab[i] > 2147483647 && tab[i] < -2147483648)
			return (0);
		i++;
	}
	return (1);
}
